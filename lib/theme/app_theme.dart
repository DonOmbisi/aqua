  /// Get status color based on water quality level
  static Color getWaterQualityColor(String status) {
    switch (status.toLowerCase()) {
      case 'excellent':
        return excellentWaterQuality;
      case 'good':
        return goodWaterQuality;
      case 'moderate':
        return moderateWaterQuality;
      case 'poor':
        return poorWaterQuality;
      case 'critical':
        return criticalWaterQuality;
      default:
        return moderateWaterQuality;
    }
  }
